/*
 
سؤال
صمم برنامج يطلب من المستخدم إدخال رقم
 ثم يحسب أكبر رقم يمكن الوصول إليه من خلال ضرب الأرقام من 1 إلى هذا الرقم  لكن إذا وصلت النتيجة إلى رقم أكبر من 100000
 يوقف البرنامج ويطبع رسالة مناسبة.
 (هذا يعني loop + if داخل loop)
*/
#include <iostream>
using namespace std;
int main () {
    int n;
    cout << "أدخل رقمًا: ";
    cin >> n;

    long long result = 1;
    for (int i = 1; i <= n; i++) {
        result *= i;
        if (result > 100000) {
            cout << "النتيجة تجاوزت 100000 عند الرقم " << i << ". التوقف عن الحساب." << endl;
            return 0;
        }
    }

    cout << "أكبر رقم يمكن الوصول إليه هو: " << result << endl;
    return 0;
}